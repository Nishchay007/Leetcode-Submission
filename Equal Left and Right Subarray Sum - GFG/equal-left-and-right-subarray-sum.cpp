class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        // code here
        int x=0,y=0;
        vector<int> p(N,0),s(N,0);
        for(int i=0;i<N;i++){
            x+=A[i];
            p[i]=x;
            y+=A[N-i-1];
            s[N-i-1]=y;
        }
        for(int i=0;i<N;i++){
            if(p[i]==s[i])
            return i+1;
        }
        return -1;
       
    }
};


